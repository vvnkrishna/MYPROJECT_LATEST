package JavaPackage;

public class SwitchCase1 {

	public static void main(String[] args) {

		int number = 25;

		switch (number) {

		// case statement

		case 15:
			System.out.println("Not Matched it is 15");
			break;

		case 18:
			System.out.println("Not Matched it is 18");
			break;

		case 10:
			System.out.println("Not Matched it is 10");
			break;

		default:
			System.out.println("Nothing Matched");
			break;

		case 20:
			System.out.println("Not Matched it is 20");
			break;

		case 25:
			System.out.println("Matched it is 25");
			break;

		case 19:
			System.out.println("Not Matched it is 19");

		}

	}

}
